ll fibo(int n){
	if(n==0){
		return 0;
	}
	else if(n == 1){
		return 1;
	}
	else{
		if(memo[n]!=-1){
			return memo[n];
		}

		return memo[n] = fibo(n-1)+fibo(n-2);
	}

}

